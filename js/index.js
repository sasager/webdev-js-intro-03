// Prevent us from attempting to use variables
// that are not declared
"use strict"

console.log(1 >= 3); //output: false

console.log(2 != 7); //output: true

console.log( 2 || 6 || 8); //output: 2

console.log(2 && 6 && 8); //output: 8

console.log(15 / 3); //output: 5

console.log(15 / 6); //output: 2.5

console.log(15 % 6); //output: 3

console.log(15 % 3); //output: 0

console.log(20 == 10 + "10") //output: false

console.log(20 == 10 + 10); //output: true